    private void jComboBox2ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox2ItemStateChanged
        String s=(String)jComboBox2.getSelectedItem();
        if(s.equals("Student"))
        {
            new StuDetails().setVisible(true);
            dispose();
        }
        else if(s.equals("Faculty"))
        {
            new FacultyDetails().setVisible(true);
            dispose();
        }
        else if(s.equals("Batch"))
        {
            new BatchDetails().setVisible(true);
            dispose();
        }
        else if(s.equals("Subject"))
        {
            new SubDetails().setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_jComboBox2ItemStateChanged

